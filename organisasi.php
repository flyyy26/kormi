    // Tambahkan event listener untuk setiap tombol
    document.querySelectorAll('.tabButton').forEach(button => {
        button.addEventListener('click', function() {
            // Dapatkan tab yang dipilih
            var selectedTab = this.getAttribute('data-tab');

            // Sembunyikan semua konten tab
            var tabContents = document.querySelectorAll('.content_struktur');
            tabContents.forEach(function(content) {
                content.style.display = 'none';
            });

            // Tampilkan tab yang dipilih
            document.getElementById(selectedTab).style.display = 'grid';
        });
    });

    // Tampilkan tab pertama secara default
    document.getElementById('tab1').style.display = 'grid';
</script>



